package com.mycompany.myapp.entities;

import java.util.Objects;

/**
 *
 * @author admin
 */
public class Event {
        private int id;
        private String titre;
        private String image;

    public Event(String image) {
        this.image = image;
    }

        
    public Event() {
    }

    public Event(int id, String titre, String image) {
        this.id = id;
        this.titre = titre;
        this.image = image;
    }

    public Event(String titre, String image) {
        this.titre = titre;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre(String titre) {
        this.titre = titre;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 73 * hash + this.id;
        hash = 73 * hash + Objects.hashCode(this.titre);
        hash = 73 * hash + Objects.hashCode(this.image);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Event other = (Event) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.titre, other.titre)) {
            return false;
        }
        if (!Objects.equals(this.image, other.image)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Event{" + "titre=" + titre + ", image=" + image + '}';
    }
}
